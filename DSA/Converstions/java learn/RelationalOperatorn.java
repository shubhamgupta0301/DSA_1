public class RelationalOperatorn {
    public static void main(String [] args){

        int a = 25, b = 40;

        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a > b); // false
        System.out.println(a < b); // true
        System.out.println(a >= b); //false
        System.out.println(a <= b); // true
    }
}
